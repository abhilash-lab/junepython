#relational operator
num1=100

num2 =200

print(num1>num2)#100>200 false

print(num1<num2)#100<200 true

print(num1>=num2)#100>=200 false

print(num1<=num2)#100<=200 true

print(num1==num2)#100==200 false

print(num1!=num2)#100!=200 true

print(num1%100==0)# to find century year(ends with 00)

print(num1>0)#to find +ve number

print(num1<0)#to find -ve number

print(num1%2==0)#to find even

print(num1%2!=0)# to find odd